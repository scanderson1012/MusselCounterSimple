# Model helpers


def create_model(num_classes: int) -> torch.nn.Module:
    model = fasterrcnn_resnet50_fpn_v2(weights=None, weights_backbone=None)
    in_features = model.roi_heads.box_predictor.cls_score.in_features
    model.roi_heads.box_predictor = FastRCNNPredictor(in_features, num_classes)
    return model


def load_model_from_checkpoint(config: FineTuneConfig, device: torch.device) -> torch.nn.Module:
    model = create_model(config.num_classes)
    state_dict = torch.load(config.old_model_path, map_location=device)
    model.load_state_dict(state_dict)
    model.to(device)
    return model


def configure_trainable_parameters(model: torch.nn.Module, freeze_backbone: bool) -> list[torch.nn.Parameter]:
    if freeze_backbone:
        for parameter in model.backbone.parameters():
            parameter.requires_grad = False
    else:
        for parameter in model.parameters():
            parameter.requires_grad = True
    return [parameter for parameter in model.parameters() if parameter.requires_grad]


# %%
# Training and evaluation


def move_batch_to_device(
    images: Iterable[torch.Tensor],
    targets: Iterable[dict[str, torch.Tensor]],
    device: torch.device,
) -> tuple[list[torch.Tensor], list[dict[str, torch.Tensor]]]:
    image_list = [image.to(device) for image in images]
    target_list = [{key: value.to(device) for key, value in target.items()} for target in targets]
    return image_list, target_list


def train_one_epoch(
    model: torch.nn.Module,
    loader: DataLoader,
    optimizer: torch.optim.Optimizer,
    device: torch.device,
) -> float:
    model.train()
    running_loss = 0.0

    progress = tqdm(loader, desc="Training", leave=False)
    for images, targets in progress:
        images, targets = move_batch_to_device(images, targets, device)
        loss_dict = model(images, targets)
        total_loss = sum(loss for loss in loss_dict.values())

        optimizer.zero_grad()
        total_loss.backward()
        optimizer.step()

        running_loss += total_loss.item()
        progress.set_postfix(loss=f"{total_loss.item():.4f}")

    return running_loss / max(len(loader), 1)


@torch.no_grad()
def compute_detection_loss(
    model: torch.nn.Module,
    loader: DataLoader,
    device: torch.device,
) -> float:
    """Compute detection loss on held-out data without optimizer updates.

    Torchvision detection models only return losses in training mode. We switch
    modes temporarily, keep gradients disabled, and restore the original mode.
    """
    was_training = model.training
    model.train()
    running_loss = 0.0

    for images, targets in loader:
        images, targets = move_batch_to_device(images, targets, device)
        loss_dict = model(images, targets)
        total_loss = sum(loss for loss in loss_dict.values())
        running_loss += total_loss.item()

    model.train(was_training)
    return running_loss / max(len(loader), 1)


@torch.no_grad()
def compute_detection_metrics(
    model: torch.nn.Module,
    loader: DataLoader,
    device: torch.device,
    score_threshold: float,
    iou_threshold: float,
) -> dict[str, float]:
    model.eval()
    map_metric = MeanAveragePrecision(
        box_format="xyxy",
        iou_type="bbox",
        class_metrics=False,
    ).to(device)

    total_tp = 0
    total_fp = 0
    total_fn = 0

    progress = tqdm(loader, desc="Evaluating", leave=False)
    for images, targets in progress:
        images, targets = move_batch_to_device(images, targets, device)
        outputs = model(images)

        preds_for_map = []
        gts_for_map = []

        for output, target in zip(outputs, targets):
            preds_for_map.append(
                {
                    "boxes": output["boxes"].detach(),
                    "scores": output["scores"].detach(),
                    "labels": output["labels"].detach(),
                }
            )
            gts_for_map.append(
                {
                    "boxes": target["boxes"].detach(),
                    "labels": target["labels"].detach(),
                }
            )

            filtered = output["scores"] >= score_threshold
            pred_boxes = output["boxes"][filtered].detach().cpu()
            pred_labels = output["labels"][filtered].detach().cpu()
            gt_boxes = target["boxes"].detach().cpu()
            gt_labels = target["labels"].detach().cpu()

            image_tp, image_fp, image_fn = match_detections(
                pred_boxes,
                pred_labels,
                gt_boxes,
                gt_labels,
                iou_threshold,
            )
            total_tp += image_tp
            total_fp += image_fp
            total_fn += image_fn

        map_metric.update(preds_for_map, gts_for_map)

    map_results = map_metric.compute()
    precision = total_tp / (total_tp + total_fp) if (total_tp + total_fp) > 0 else 0.0
    recall = total_tp / (total_tp + total_fn) if (total_tp + total_fn) > 0 else 0.0
    f1 = (2 * precision * recall / (precision + recall)) if (precision + recall) > 0 else 0.0

    return {
        "map": float(map_results["map"].item()),
        "map_50": float(map_results["map_50"].item()),
        "map_75": float(map_results["map_75"].item()),
        "mar_100": float(map_results["mar_100"].item()),
        "precision": float(precision),
        "recall": float(recall),
        "f1": float(f1),
    }


def match_detections(
    pred_boxes: torch.Tensor,
    pred_labels: torch.Tensor,
    gt_boxes: torch.Tensor,
    gt_labels: torch.Tensor,
    iou_threshold: float,
) -> tuple[int, int, int]:
    if len(pred_boxes) == 0 and len(gt_boxes) == 0:
        return 0, 0, 0
    if len(pred_boxes) == 0:
        return 0, 0, len(gt_boxes)
    if len(gt_boxes) == 0:
        return 0, len(pred_boxes), 0

    matched_pred: set[int] = set()
    matched_gt: set[int] = set()

    unique_labels = sorted(set(gt_labels.tolist()) | set(pred_labels.tolist()))
    for class_id in unique_labels:
        pred_mask = pred_labels == class_id
        gt_mask = gt_labels == class_id

        class_pred_boxes = pred_boxes[pred_mask]
        class_gt_boxes = gt_boxes[gt_mask]
        pred_indices = pred_mask.nonzero(as_tuple=False).flatten().tolist()
        gt_indices = gt_mask.nonzero(as_tuple=False).flatten().tolist()

        if len(class_pred_boxes) == 0 or len(class_gt_boxes) == 0:
            continue

        ious = box_iou(class_pred_boxes, class_gt_boxes)
        candidate_matches: list[tuple[float, int, int]] = []
        for pred_pos in range(ious.shape[0]):
            for gt_pos in range(ious.shape[1]):
                iou_value = float(ious[pred_pos, gt_pos].item())
                if iou_value >= iou_threshold:
                    candidate_matches.append((iou_value, pred_indices[pred_pos], gt_indices[gt_pos]))

        candidate_matches.sort(reverse=True, key=lambda row: row[0])
        for _, pred_index, gt_index in candidate_matches:
            if pred_index not in matched_pred and gt_index not in matched_gt:
                matched_pred.add(pred_index)
                matched_gt.add(gt_index)

    tp = len(matched_pred)
    fp = len(pred_boxes) - tp
    fn = len(gt_boxes) - len(matched_gt)
    return tp, fp, fn


def evaluate_model(
    model: torch.nn.Module,
    loader: DataLoader,
    device: torch.device,
    config: FineTuneConfig,
) -> dict[str, float]:
    metrics = compute_detection_metrics(
        model=model,
        loader=loader,
        device=device,
        score_threshold=config.score_threshold,
        iou_threshold=config.iou_threshold,
    )
    metrics["loss"] = compute_detection_loss(model=model, loader=loader, device=device)
    return metrics


