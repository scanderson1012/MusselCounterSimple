def run_single_finetuning_experiment(
    config: FineTuneConfig,
    num_new_samples: int,
) -> dict[str, Any]:
    validate_paths(config)
    assert_no_data_leakage(config)
    set_seed(config.seed)

    device = choose_device()
    print(f"\nNow running fine-tuning with {num_new_samples} new samples")
    print(f"Using device: {device}")

    datasets = build_datasets(config)
    train_dataset, subset_summary = build_train_dataset(
        old_train_dataset=datasets["old_train"],
        new_train_dataset=datasets["new_train"],
        num_new_samples=num_new_samples,
        config=config,
    )
    print(
        "Training split summary: "
        f"{subset_summary['num_new_samples']} new + "
        f"{subset_summary['replay_size']} replay = "
        f"{subset_summary['total_train_samples']} total samples"
    )

    train_loader, test_loader = build_dataloaders(
        train_dataset=train_dataset,
        test_dataset=datasets["test"],
        config=config,
    )
    print(f"Batch size: {config.batch_size}")
    print("Evaluating the old checkpoint model on the held-out test set before fine-tuning...")

    baseline_model = load_model_from_checkpoint(config, device)
    baseline_metrics = evaluate_model(baseline_model, test_loader, device, config)
    print(
        "Old model test metrics: "
        f"loss={baseline_metrics['loss']:.4f}, "
        f"mAP@50={baseline_metrics['map_50']:.4f}, "
        f"precision={baseline_metrics['precision']:.4f}, "
        f"recall={baseline_metrics['recall']:.4f}, "
        f"f1={baseline_metrics['f1']:.4f}"
    )

    finetune_model = copy.deepcopy(baseline_model)
    trainable_parameters = configure_trainable_parameters(
        finetune_model,
        freeze_backbone=config.freeze_backbone,
    )
    optimizer = torch.optim.AdamW(
        trainable_parameters,
        lr=config.learning_rate,
        weight_decay=config.weight_decay,
    )

    epoch_history: list[dict[str, Any]] = []
    total_start = time.perf_counter()

    for epoch in range(1, config.num_epochs + 1):
        epoch_start = time.perf_counter()
        train_loss = train_one_epoch(finetune_model, train_loader, optimizer, device)
        test_metrics = evaluate_model(finetune_model, test_loader, device, config)
        epoch_seconds = time.perf_counter() - epoch_start

        epoch_history.append(
            {
                "epoch": epoch,
                "train_loss": train_loss,
                "test_loss": test_metrics["loss"],
                "test_map": test_metrics["map"],
                "test_map_50": test_metrics["map_50"],
                "test_map_75": test_metrics["map_75"],
                "test_mar_100": test_metrics["mar_100"],
                "test_precision": test_metrics["precision"],
                "test_recall": test_metrics["recall"],
                "test_f1": test_metrics["f1"],
                "epoch_seconds": epoch_seconds,
            }
        )

        print(
            f"Epoch {epoch}/{config.num_epochs} | "
            f"train_loss={train_loss:.4f} | "
            f"test_loss={test_metrics['loss']:.4f} | "
            f"test_map50={test_metrics['map_50']:.4f} | "
            f"time={epoch_seconds:.2f}s"
        )

    total_seconds = time.perf_counter() - total_start

    final_metrics = epoch_history[-1].copy() if epoch_history else {}
    return {
        "config": asdict(config),
        "device": str(device),
        "subset_summary": subset_summary,
        "baseline_test_metrics": baseline_metrics,
        "epoch_history": epoch_history,
        "final_epoch_metrics": final_metrics,
        "total_seconds": total_seconds,
        "saved_model_path": None,
        "notes": (
            "Test metrics are logged each epoch for comparison, but they are not "
            "used for checkpoint selection to reduce test-set leakage. "
            "Model weights are not saved in this testing version."
        ),
    }


def benchmark_new_data_sizes(
    config: FineTuneConfig,
    new_sample_sizes: list[int],
) -> dict[str, Any]:
    datasets = build_datasets(config)
    max_available = len(datasets["new_train"])
    requested_sizes = []
    for sample_size in new_sample_sizes:
        if sample_size > max_available:
            raise ValueError(
                f"Requested {sample_size} new images, but only {max_available} are available."
            )
        requested_sizes.append(sample_size)

    experiments: list[dict[str, Any]] = []
    summary_rows: list[dict[str, Any]] = []

