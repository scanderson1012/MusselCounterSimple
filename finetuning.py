def parse_voc_xml(xml_path: Path, class_name_to_id: dict[str, int]) -> tuple[list[list[float]], list[int]]:
    tree = ET.parse(xml_path)
    root = tree.getroot()

    boxes: list[list[float]] = []
    labels: list[int] = []

    for obj in root.findall("object"):
        class_node = obj.find("name")
        bbox_node = obj.find("bndbox")
        if class_node is None or bbox_node is None:
            continue

        class_name = class_node.text.strip().lower()
        if class_name not in class_name_to_id:
            continue

        xmin = float(bbox_node.find("xmin").text)
        ymin = float(bbox_node.find("ymin").text)
        xmax = float(bbox_node.find("xmax").text)
        ymax = float(bbox_node.find("ymax").text)

        if xmax <= xmin or ymax <= ymin:
            continue

        boxes.append([xmin, ymin, xmax, ymax])
        labels.append(class_name_to_id[class_name])

    return boxes, labels


class VOCDataset(Dataset):
    def __init__(
        self,
        images_dir: Path,
        labels_dir: Path,
        class_name_to_id: dict[str, int],
        transforms: A.Compose | None = None,
    ) -> None:
        self.images_dir = Path(images_dir)
        self.labels_dir = Path(labels_dir)
        self.class_name_to_id = class_name_to_id
        self.transforms = transforms
        valid_exts = {".jpg", ".jpeg", ".png", ".bmp"}

        self.samples: list[tuple[Path, Path]] = []
        for image_path in sorted(self.images_dir.iterdir()):
            if image_path.suffix.lower() not in valid_exts:
                continue
            xml_path = self.labels_dir / f"{image_path.stem}.xml"
            if xml_path.exists():
                self.samples.append((image_path, xml_path))

        print(f"Loaded {len(self.samples)} samples from {self.images_dir}")

    def __len__(self) -> int:
        return len(self.samples)

    def __getitem__(self, index: int) -> tuple[torch.Tensor, dict[str, torch.Tensor]]:
        image_path, xml_path = self.samples[index]
        image_np = np.array(Image.open(image_path).convert("RGB"))
        boxes, labels = parse_voc_xml(xml_path, self.class_name_to_id)

        boxes_np = np.array(boxes, dtype=np.float32) if boxes else np.zeros((0, 4), dtype=np.float32)
        labels_np = np.array(labels, dtype=np.int64) if labels else np.zeros((0,), dtype=np.int64)

        if self.transforms is not None:
            transformed = self.transforms(
                image=image_np,
                bboxes=boxes_np.tolist(),
                labels=labels_np.tolist(),
            )
            image = transformed["image"]
            boxes_np = (
                np.array(transformed["bboxes"], dtype=np.float32)
                if transformed["bboxes"]
                else np.zeros((0, 4), dtype=np.float32)
            )
            labels_np = (
                np.array(transformed["labels"], dtype=np.int64)
                if transformed["labels"]
                else np.zeros((0,), dtype=np.int64)
            )
        else:
            image = ToTensorV2()(image=image_np)["image"]

        image = image.float()
        if image.max() > 1.0:
            image = image / 255.0

        boxes_tensor = torch.as_tensor(boxes_np, dtype=torch.float32)
        labels_tensor = torch.as_tensor(labels_np, dtype=torch.int64)
        area_tensor = (
            (boxes_tensor[:, 2] - boxes_tensor[:, 0]) * (boxes_tensor[:, 3] - boxes_tensor[:, 1])
            if len(boxes_tensor) > 0
            else torch.zeros((0,), dtype=torch.float32)
        )

        target = {
            "boxes": boxes_tensor,
            "labels": labels_tensor,
            "image_id": torch.tensor([index], dtype=torch.int64),
            "area": area_tensor,
            "iscrowd": torch.zeros((len(labels_tensor),), dtype=torch.int64),
        }
        return image, target


