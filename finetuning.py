@dataclass(slots=True)
class FineTuneConfig:
    train_dir: Path
    test_dir: Path
    new_dir: Path
    old_model_path: Path
    class_name_to_id: dict[str, int]
    image_size: int = 640
    num_epochs: int = 5
    learning_rate: float = 1e-4
    weight_decay: float = 1e-4
    batch_size: int = 4
    num_workers: int = 0
    seed: int = 42
    freeze_backbone: bool = True
    score_threshold: float = 0.50
    iou_threshold: float = 0.50

    @property
    def num_classes(self) -> int:
        return len(self.class_name_to_id) + 1


# %%
# Utilities


def set_seed(seed: int) -> None:
    random.seed(seed)
    np.random.seed(seed)
    torch.manual_seed(seed)
    if torch.cuda.is_available():
        torch.cuda.manual_seed_all(seed)


def choose_device() -> torch.device:
    return torch.device("cuda" if torch.cuda.is_available() else "cpu")


def collate_fn(batch: list[tuple[torch.Tensor, dict[str, torch.Tensor]]]) -> tuple:
    return tuple(zip(*batch))


def validate_paths(config: FineTuneConfig) -> None:
    required_dirs = [
        config.train_dir,
        config.test_dir,
        config.new_dir,
    ]
    for directory in required_dirs:
        if not directory.exists():
            raise FileNotFoundError(f"Missing directory: {directory}")
        images_dir = directory / "images"
        labels_dir = directory / "labels"
        if not images_dir.exists():
            raise FileNotFoundError(f"Missing images directory: {images_dir}")
        if not labels_dir.exists():
            raise FileNotFoundError(f"Missing labels directory: {labels_dir}")
    if not config.old_model_path.is_file():
        raise FileNotFoundError(f"Missing model checkpoint: {config.old_model_path}")


def image_stems_in_dir(root_dir: Path) -> set[str]:
    valid_exts = {".jpg", ".jpeg", ".png", ".bmp"}
    return {
        path.stem
        for path in root_dir.iterdir()
        if path.is_file() and path.suffix.lower() in valid_exts
    }


def assert_no_data_leakage(config: FineTuneConfig) -> None:
    old_train_stems = image_stems_in_dir(config.train_dir / "images")
    test_stems = image_stems_in_dir(config.test_dir / "images")
    new_stems = image_stems_in_dir(config.new_dir / "images")

    overlaps = {
        "old_train vs test": old_train_stems & test_stems,
        "new_data vs test": new_stems & test_stems,
    }
    leaked = {name: stems for name, stems in overlaps.items() if stems}
    if leaked:
        overlap_summary = ", ".join(
            f"{name} ({len(stems)} overlaps)" for name, stems in leaked.items()
        )
        raise ValueError(
            "Potential data leakage detected between training and test folders: "
            f"{overlap_summary}"
        )


def sample_indices(total_size: int, sample_size: int, seed: int) -> list[int]:
    if sample_size > total_size:
        raise ValueError(f"Requested {sample_size} samples, but only {total_size} exist.")
    rng = random.Random(seed)
    return rng.sample(range(total_size), sample_size)


def recommend_replay_size(
    num_new_samples: int,
    old_train_size: int,
) -> int:
    """Use a simple 1:1 replay policy based on the new-data subset size."""
    if old_train_size <= 0:
        return 0

    return min(old_train_size, max(1, num_new_samples))


def maybe_to_dataframe(records: list[dict[str, Any]]) -> Any:
    if pd is None:
        return records
    return pd.DataFrame(records)


