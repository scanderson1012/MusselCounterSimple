# Transforms and loaders


def get_train_transforms(image_size: int) -> A.Compose:
    return A.Compose(
        [
            A.Resize(height=image_size, width=image_size),
            A.HorizontalFlip(p=0.5),
            A.Rotate(limit=15, p=0.5, border_mode=0),
            A.RandomBrightnessContrast(p=0.3),
            A.Affine(scale=(0.95, 1.05), translate_percent=(0.0, 0.05), p=0.3),
            ToTensorV2(),
        ],
        bbox_params=A.BboxParams(
            format="pascal_voc",
            label_fields=["labels"],
            min_visibility=0.1,
        ),
    )


def get_eval_transforms(image_size: int) -> A.Compose:
    return A.Compose(
        [
            A.Resize(height=image_size, width=image_size),
            ToTensorV2(),
        ],
        bbox_params=A.BboxParams(
            format="pascal_voc",
            label_fields=["labels"],
            min_visibility=0.0,
        ),
    )


def build_datasets(config: FineTuneConfig) -> dict[str, Dataset]:
    train_tfms = get_train_transforms(config.image_size)
    eval_tfms = get_eval_transforms(config.image_size)
    return {
        "old_train": VOCDataset(
            images_dir=config.train_dir / "images",
            labels_dir=config.train_dir / "labels",
            class_name_to_id=config.class_name_to_id,
            transforms=train_tfms,
        ),
        "test": VOCDataset(
            images_dir=config.test_dir / "images",
            labels_dir=config.test_dir / "labels",
            class_name_to_id=config.class_name_to_id,
            transforms=eval_tfms,
        ),
        "new_train": VOCDataset(
            images_dir=config.new_dir / "images",
            labels_dir=config.new_dir / "labels",
            class_name_to_id=config.class_name_to_id,
            transforms=train_tfms,
        ),
    }


def build_train_dataset(
    old_train_dataset: Dataset,
    new_train_dataset: Dataset,
    num_new_samples: int,
    config: FineTuneConfig,
) -> tuple[Dataset, dict[str, Any]]:
    new_indices = sample_indices(len(new_train_dataset), num_new_samples, config.seed)
    replay_size = recommend_replay_size(num_new_samples, len(old_train_dataset))
    replay_indices = sample_indices(len(old_train_dataset), replay_size, config.seed + 1)

    new_subset = Subset(new_train_dataset, new_indices)
    replay_subset = Subset(old_train_dataset, replay_indices)
    train_dataset = ConcatDataset([new_subset, replay_subset])

    subset_summary = {
        "num_new_samples": num_new_samples,
        "replay_size": replay_size,
        "total_train_samples": len(train_dataset),
        "new_indices": new_indices,
        "replay_indices": replay_indices,
    }
    return train_dataset, subset_summary


def build_dataloaders(
    train_dataset: Dataset,
    test_dataset: Dataset,
    config: FineTuneConfig,
) -> tuple[DataLoader, DataLoader]:
    train_loader = DataLoader(
        train_dataset,
        batch_size=config.batch_size,
        shuffle=True,
        num_workers=config.num_workers,
        collate_fn=collate_fn,
    )
    test_loader = DataLoader(
        test_dataset,
        batch_size=config.batch_size,
        shuffle=False,
        num_workers=config.num_workers,
        collate_fn=collate_fn,
    )
    return train_loader, test_loader


